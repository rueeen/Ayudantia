# Ejemplo input()

# input() es una funcion como print()
# sirve para recibir datos desde la consola o terminal
# y su retorno SIEMPRE es STR

nombre = input('Ingrese su nombre: ') # Se muestra por consola mensaje 'Ingrese su nombre' y se guarda lo que se escribio en variable nombre

print('Hola, tu nombre es', nombre)

numero1 = input('Ingrese un numero: ') # 2 str
numero2 = input('Ingrese otro numero: ') # 3 str
# + hace concatenacion entre str
print('La suma de los 2 numeros es:', numero1 + numero2) # 23